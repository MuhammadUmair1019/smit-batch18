// =================================================
// VARIABLES IN JAVASCRIPT
// =================================================

// Variables are used to store information
// This information can be reused or modified later

// -------------------------------------------------
// RULES FOR NAMING VARIABLES
// -------------------------------------------------

// ❌ Invalid variable names
// var 1x = 10;          // Cannot start with a number
// var &y = 20;          // Special characters are not allowed
// var first name = 'Ali'; // Spaces are not allowed

// ✅ Valid variable names
// var $firstName = "Ahmed";
// var _lastName = "Ali";

// -------------------------------------------------
// NAMING CONVENTIONS
// -------------------------------------------------

// 1. Snake Case (commonly used in some languages)
var first_name = "Ali";

// 2. Camel Case (MOST COMMON in JavaScript)
var firstName = "Ali";

// -------------------------------------------------
// VARIABLE STRUCTURE
// -------------------------------------------------

// var    → keyword used to declare a variable
// x      → variable name
// 10     → variable value

var x = 10;                // number
var userName = "Muhammad"; // string
var age = "10";            // string (because it is inside quotes)
var isLoggedIn = true;      // boolean

// -------------------------------------------------
// PRINTING OUTPUT
// -------------------------------------------------

// console.log() prints output in the browser console
console.log(x);
console.log("Hello, World!");

// -------------------------------------------------
// ALERT POPUP
// -------------------------------------------------

// alert() shows a popup message in the browser
alert("Hello, World!");
